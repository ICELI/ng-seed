#!/bin/sh
# Update AUTHORS.md based on git history.

git log --reverse --format='%aN (%aE)' | perl -we '
BEGIN {
  %seen = (), @authors = ();
}
while (<>) {
  next if $seen{$_};
  next if /(ti10mylove\@163.com)/;
  next if /(Yibang Yang)/;
  next if /(jumore.com)/;
  next if /(ç«‹)/;
  $seen{$_} = push @authors, "- ", $_;
}
END {
  print "# Authors\n\n";
  print "#### Ordered by first contribution.\n\n";
  print @authors, "\n";
  print "#### Generated by build/update-authors.sh.\n";
}
' > AUTHORS.md